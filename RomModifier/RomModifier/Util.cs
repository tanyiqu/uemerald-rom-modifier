    public static class Util
    {
        //返回指定文件filePath的offset偏移处的1字节的内容
        public static byte ReadByte(string filePath, long offset)
        {
            byte[] buf = new byte[] { 0xFF };

            FileStream stream = File.OpenRead(filePath);

            stream.Seek(offset, SeekOrigin.Begin);

            stream.Read(buf, 0, 1);

            stream.Close();

            return buf[0];
        }

        //向指定文件指定文件filePath的offset偏移处覆盖写入size字节的buf的内容
        public static bool WriteByte(string filePath, long offset, byte[] buf, int size)
        {
            bool isSucceed = false;

            if (buf.Length < size) return isSucceed;

            FileStream stream = File.OpenWrite(filePath);// 以写的方式打开

            stream.Seek(offset, SeekOrigin.Begin);//偏移到指定位

            stream.Write(buf, 0, size);

            stream.Close();

            isSucceed = true;

            return isSucceed;
        }

        //向指定文件指定文件filePath的offset偏移处写入1字节的buf的内容
        public static bool WriteByte(string filePath, long offset, byte buf)
        {
            bool isSucceed = false;

            byte[] array = new byte[] { buf };

            FileStream stream = File.OpenWrite(filePath);

            stream.Seek(offset, SeekOrigin.Begin);//偏移到指定位

            stream.Write(array, 0, 1);

            stream.Close();

            isSucceed = true;

            return isSucceed;
        }

    }

